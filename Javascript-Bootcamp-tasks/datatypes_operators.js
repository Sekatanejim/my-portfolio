// Exercise 1: Arithmetic Operations
let num1 = 15;
let num2 = 5;

let sum = num1 + num2;
let difference = num1 - num2;
let product = num1 * num2;
let quotient = num1 / num2;

console.log("Sum:", sum);
console.log("Difference:", difference);
console.log("Product:", product);
console.log("Quotient:", quotient);

// Exercise 2: Numbers
let integerNum = 10;
let floatNum = 3.5;

let add = integerNum + floatNum;
let subtract = integerNum - floatNum;
let multiply = integerNum * floatNum;
let divide = integerNum / floatNum;
let modulus = integerNum % 3;
let exponent = integerNum ** 2;

console.log("Addition:", add);
console.log("Subtraction:", subtract);
console.log("Multiplication:", multiply);
console.log("Division:", divide);
console.log("Modulus:", modulus);
console.log("Exponentiation:", exponent);

// Exercise 3: Boolean and Operators
let numA = 8;
let numB = 12;

let isGreater = numA > numB;
let isLessOrEqual = numA <= numB;
let isEqual = numA === numB;
let isNotEqual = numA !== numB;

console.log("Is 8 > 12?", isGreater);
console.log("Is 8 <= 12?", isLessOrEqual);
console.log("Is 8 == 12?", isEqual);
console.log("Is 8 != 12?", isNotEqual);

// Logical operations
let a = true;
let b = false;

console.log("a AND b:", a && b);
console.log("a OR b:", a || b);
console.log("NOT a:", !a);

// Assignment operators
let p = 10;
p += 5;
console.log("After += 5:", p);

p -= 3;
console.log("After -= 3:", p);

p *= 2;
console.log("After *= 2:", p);

p /= 4;
console.log("After /= 4:", p);

p %= 3;
console.log("After %= 3:", p);
